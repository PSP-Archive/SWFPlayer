char *	skipWS( char * s )
{
	while ( ( *s ) == ' ' || ( *s ) == '\t' )
	{
		++s;
	}

	return s;
}

//**********************************************************************************
//
//**********************************************************************************
void	trimEndingChar( char * s, char c )
{
	char *	p = s + strlen( s ) - 1;

	while ( p >= s && ( ( *p ) == c ) )
	{
		--p;
	}

	( *( ++p ) ) = 0;
}

//**********************************************************************************
//
//**********************************************************************************
void	trimEndingWS( char * s )
{
	char *	p = s + strlen( s ) - 1;

	while ( p >= s && ( ( *p ) == ' ' || ( *p ) == '\t' ) )
	{
		--p;
	}

	( *( ++p ) ) = 0;
}

